      var Messenger = function(el, msgTexts,colors, initial){
        'use strict';
        var m = this;
        
        m.init = function(){
          m.codeletters = initial;
          m.message = 0;
          m.current_length = 0;
          m.fadeBuffer = false;
          m.messages = msgTexts;
          m.colors = colors;
          $(el).css("color",m.colors[m.message]);
          setTimeout(m.animateIn, 100);
        };
        
        // m.generateRandomString = function(length){
