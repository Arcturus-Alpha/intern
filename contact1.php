            setTimeout(m.cycleText, 1000/m.messages[m.message].length);
          } else { 
            m.message = m.message + 1;
          
          if(m.message >= m.messages.length){
            m.message = 0;
          }
          $(el).css("color",m.colors[m.message]);
          m.current_length = 0;
          m.fadeBuffer = false;
          setTimeout(m.animateIn, 200);
          }
          
          
        };
        
        m.init();
